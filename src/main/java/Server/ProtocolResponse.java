package Server;

/**
 * This enum represents the possible response types in the server-client protocol.
 * It defines the different types of responses that the server can send to the client.
 *
 * @author Pascal von Fellenberg
 * @author Istref Uka
 */
public enum ProtocolResponse {
  PWIN,
  EMPT,
  CATS,
  PING,
}
